	private int CountTriangles(Mesh mesh, int vertexIndex) {
		int count = 0;
		for (int j=0; j<mesh.triangles.Length; j++) {
			if (mesh.triangles[j] == vertexIndex) {
				count++;
			}
		}
		return count;
	}
	
